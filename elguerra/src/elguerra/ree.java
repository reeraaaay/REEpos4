    void getcash(){
        String no1 = jTextField1.getText();
        String no2 = jLabel3.getText();
        String no3 = jTextField4.getText();
        
        if (no1.equals("")){
            jTextField2.setText("");
        }
        else {
            Double dig1 = Double.parseDouble (no1);
            Double total = Double.parseDouble(no2);
            Double quantity = Double.parseDouble(no3);
            Double overall = total*quantity;
            Double change = dig1 - overall;
            jTextField2.setText(change.toString());
            
        }
    }
