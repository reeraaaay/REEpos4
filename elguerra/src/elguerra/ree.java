    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        
        int item = jComboBox1.getSelectedIndex();
        
        if (item == 0) {
            setprod("Apple");
            setprice(60d);
     
        }
        else if (item == 1) {
            setprod("Banana");
            setprice(50d);
        }
        else if (item == 2) {
            setprod("Eggplant");
            setprice(30d);
        }
        else if (item == 3){
            setprod("Mango");
            setprice(70d);
        }
        else if (item == 4){
            setprod("Pineapple");
            setprice(90d);
        }
        else {
        setprod("");
        setprice(0d);
    }
        jLabel6.setText(jComboBox1.getSelectedItem().toString());
        setprice(getprice());
        jLabel3.setText(getprice().toString());
    }//GEN-LAST:event_jComboBox1ActionPerformed

